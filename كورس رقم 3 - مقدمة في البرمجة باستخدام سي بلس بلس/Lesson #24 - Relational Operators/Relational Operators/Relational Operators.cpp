//(Compare (مقارنة)),(Relational Operators (العوامل العلائقية))
//the Relational Operators in C++ are used to compare two values 
#include <iostream>
using namespace std;
int main()
{
	int A = 10, B = 20;

	cout << (A == B) << endl;
	cout << (A != B) << endl;
	cout << (A > B) << endl;
	cout << (A < B) << endl;
	cout << (A >= B) << endl;
	cout << (A <= B) << endl;
	return 0;



}
